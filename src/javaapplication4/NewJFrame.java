    public void resetTable() {
        SanPhamDao sanphamDao = new SanPhamDao();
        sanPhamList = sanphamDao.getAllSanPham(); // gán data từ CSDL vào trong biến sanPhamList;
        //xử lý đến table 
        model = new DefaultTableModel();
        model.addColumn("Ma SP");
        model.addColumn("Tên sp");
        model.addColumn("Giá sản phẩm");
        //duyệt sanPhamList để add vào bảng  
        for(SanPham sp : sanPhamList) {
            model.addRow(new Object[]{sp.getMaSp(),sp.getTenSp(),sp.getGia()});
        }
        tblSanPham.setModel(model);
    }
