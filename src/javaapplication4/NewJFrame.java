    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        selectedRow = tblSanPham.getSelectedRow();
        // lấy thông tin từ bảng 
        String maSP = tblSanPham.getValueAt(selectedRow, 0).toString();
        String tenSP = tblSanPham.getValueAt(selectedRow, 1).toString();
        String gia = tblSanPham.getValueAt(selectedRow, 2).toString();
        // hiển thị thông tin lên input 
        txtMaSP.setText(maSP);
        txtTenSp.setText(tenSP);
        txtGia.setText(gia);
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int maSP = Integer.parseInt(txtMaSP.getText());
        String tenSP = txtTenSp.getText();
        double gia = Double.parseDouble(txtGia.getText());
        SanPham sp = new SanPham(maSP,tenSP,gia);
        SanPhamDao spDao = new SanPhamDao();
        spDao.addSanPham(sp);
        resetTable();
        //set lại text bằng rỗng
        txtMaSP.setText("");
        txtTenSp.setText("");
        txtGia.setText("");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int maSP = Integer.parseInt(txtMaSP.getText());
        String tenSP = txtTenSp.getText();
        double gia = Double.parseDouble(txtGia.getText());
        SanPham sp = new SanPham(maSP,tenSP,gia);
        SanPhamDao spDao = new SanPhamDao();
        spDao.updateSanPham(sp);
        JOptionPane.showMessageDialog(rootPane, "Sửa sản phẩm thành công");
        resetTable();
        //set lại text bằng rỗng
        setTextField();
    }//GEN-LAST:event_btnSuaActionPerformed
    public void setTextField() {
        txtMaSP.setText("");
        txtTenSp.setText("");
        txtGia.setText("");
    }
    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int maSP = Integer.parseInt(txtMaSP.getText());
        SanPhamDao spDao = new SanPhamDao();
        // xóa có confirm 
        int option = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa sp có mã là" + maSP, "Xác nhận xóa", JOptionPane.YES_OPTION);
        
        if(option == JOptionPane.YES_OPTION) { // nếu như chọn có 
         spDao.deleteSanPham(maSP);
         JOptionPane.showMessageDialog(rootPane, "Xóa sản phẩm thành công");
        } else {
         JOptionPane.showMessageDialog(rootPane, "bạn không xóa sản phẩm nữa");
        }
        setTextField();
        resetTable();
    }//GEN-LAST:event_btnXoaActionPerformed

